"""
dcf_params.py
=============
DCF (折現現金流 | Discounted Cash Flow | 割引キャッシュフロー) 估值的
「房屋觀點 (house assumptions)」集中管理。

設計原則 (對應 README 路線圖階段二):
  參數分三層,只有「營運層」隨 Bear/Base/Bull 變動,避免重複計入風險:
    1. 總經 / 房屋觀點層 (三情境固定): ERP、終值成長 g_終、無風險利率
    2. 公司結構層     (算一次固定): 稅率、資本結構、債務成本
    3. 公司營運層     (隨情境變):  FCF 成長率 g  ← 唯一該「賭」的東西

把所有靜態假設集中在此,未來階段三 (LLM) 也是覆寫這裡的數字。
所有數字皆為「判斷」,使用者應依自身觀點調整。
"""

from __future__ import annotations


# ============================================================
# 第 1 層:總經 / 房屋觀點 (三情境共用,不隨情境變)
# ============================================================

# 股權風險溢酬 (Equity Risk Premium | 株式リスクプレミアム)
# 來源: Damodaran 美股長期 ERP,約 4.5%–5.5%。屬「判斷」,建議每季人工檢視。
EQUITY_RISK_PREMIUM = 0.050          # Damodaran 2026 估計

# 終值永續成長率 (Terminal / Perpetuity Growth)
# 硬約束: 必須 < 無風險利率 (公司不可能永遠長得比經濟快) 且 < WACC (否則公式發散)
# 只有 Bull 微幅放寬,絕不超過長期名目 GDP (~3%)
TERMINAL_GROWTH = {
    "bear": 0.020,
    "base": 0.025,
    "bull": 0.030,
}

# 無風險利率 (Risk-free Rate) fetch 失敗時的後備值
RISK_FREE_FALLBACK = 0.040


# ============================================================
# 第 2 層:公司結構 (算一次,三情境固定)
# ============================================================

BETA_CLAMP = (0.5, 2.0)              # yfinance beta 很吵,夾擠到合理區間
TAX_CLAMP = (0.0, 0.25)             # 有效稅率夾擠 (虧損年會出現負值 / 破百)
TAX_FALLBACK = 0.21                 # 美國法定稅率,稅率算不出時用
COST_OF_DEBT_FALLBACK_SPREAD = 0.020  # 無利息資料時: Rd = Rf + 此利差
COST_OF_DEBT_CLAMP = (0.01, 0.15)


# ============================================================
# 第 3 層:公司營運 (隨情境變動,核心不確定性來源)
# ============================================================

# FCF 成長率預設值 (無法從歷史推算時的後備)
DEFAULT_GROWTH = {
    "bear": 0.03,
    "base": 0.08,
    "bull": 0.15,
}

# 若能從歷史 FCF 推算 CAGR,以它為 Base 錨,上下展開:
GROWTH_SPREAD_DOWN = 0.05           # bear = base − 5pp
GROWTH_SPREAD_UP = 0.05             # bull = base + 5pp
GROWTH_CLAMP = (-0.02, 0.25)        # 單一情境成長率合理上下限


# ============================================================
# 模型參數
# ============================================================

PROJECTION_YEARS = 5                # 明確預測期 (explicit forecast) 年數


# ============================================================
# 護欄 (健檢,違反就在報告印警告)
# ============================================================

WACC_SANITY = (0.06, 0.12)         # 美股大型股 WACC 合理區間
TV_SHARE_WARN = 0.80               # 終值佔企業價值 > 此值 → 警告「DCF 實質在猜終值」

# 敏感度表 (sensitivity table) 的擾動格點
SENSITIVITY_WACC_STEPS = (-0.01, 0.0, 0.01)
SENSITIVITY_TG_STEPS = (-0.005, 0.0, 0.005)
